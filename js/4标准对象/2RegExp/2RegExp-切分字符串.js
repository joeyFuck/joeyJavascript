//用正则表达式切分字符串比用固定的字符更灵活，请看正常的切分代码：
'a b   c'.split(' '); // ['a', 'b', '', '', 'c']
//嗯，无法识别连续的空格，用正则表达式试试：

'a b   c'.split(/\s+/); // ['a', 'b', 'c']
//无论多少个空格都可以正常分割。加入,试试：

'a,b, c  d'.split(/[\s\,]+/); // ['a', 'b', 'c', 'd']
//再加入;试试：

'a,b;; c  d'.split(/[\s\,\;]+/); // ['a', 'b', 'c', 'd']
//如果用户输入了一组标签，下次记得用正则表达式来把不规范的输入转化成正确的数组。






































